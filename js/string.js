var frase = "Japão é o melhor time do mundo";

//funções string
console.log("Frase original: " + frase);
console.log("replace: " + frase.replace("Japão", "Brasil")); //troca temporariamente o 1º elemento pelo 2º
console.log("toUpperCase: " + frase.toUpperCase()); //transforma em maiuscula
console.log("toLowerCase: " + frase.toLowerCase()); //transforma em minuscula